<h1>Om skolan</h1>
<p>NTI - Gymnasiet i Södertälje befinner sig i lokaler på andra och tredje våning ovanför Willys. Adressen är Sydhamnsvägen 55-57.</p>
<p>När du börjar på vår skola kommer du få en ny bärbar dator för att underlätta dina studie i alla ämne och datorn kan köpas ut efter 3 år om det önskas. Skolan erbjuder dig möjlighet att testa andra coola saker som VR, 3D skrivare, 3D scanner, drones och du kan också träffa (robotens namn), det är en robot som (vad kan roboten göra).</p>
<p>En skoldag på NTI - Gymnasiet kan se olika ut beroende på vilken årskurs man går i. Vanligtvis börjar man 8:25 och slutar senast kl. 17:00. En lektionstillfälle är 1h 20 min. Om du har svårigheter i matematik så finns det på skolan lektionstillfälle som heter mattestuga och det är en extra tillfälle bara för matematik för dig som behöver extra hjälp. För dig som behöver extra hjälp i något annat ämne finns det lektionstillfälle som kallas för resurs, där kan du välja vilken ämne du behöver mest hjälp i.</p>
<p>De flesta rasterna är 10 minuter men det finns lunchrast som är 40 - 60 min. Det kan hända att håltimmar förekommer ibland och det man kan göra på dem långa rasterna är att spela dem olika brädspel, playstation 4 eller biljard.</p>

