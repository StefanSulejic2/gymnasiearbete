<h2>Intervju med några lärare på NTI Gymnasiet</h2>
<h3>Tobias Lindberg</h3>
<p>Tobias Lindberg är en av lärarna på NTI Gymnasiet. Han undervisar i flera olika ämne, Religion, Programmering 2, Tillämpad programmering, Webbutveckling 1, Webbserverprogrammering 1 och Digitalt skapande 1. Det kan kännas mycket vilket Tobias skulle hålla med om till en viss del men han tycker också att det är just därför han fastnar för NTI Gymnasiet i Södertälje. Tobias gillar att göra olika saker samtidigt, det är just det som gör hans jobb roligare. När frågan “Trivs du i skolan?” ställdes, svarade han direkt att han trivs väldigt bra och att han tror att alla lärare gör det eftersom det inte har skett några lärarbyte dem senaste åren. För tillfället räcker skolans lokaler till alla elever och allt som är planerat men han tycker att i snar framtid kommer skolan behöva öka utrymmet på grund av att varje läsår är det fler som söker sig till NTI Gymnasiet i Södertälje. Bakom sig har Tobias en lång karriär, han har jobbat som IT-konsult, religionslärare både i Sverige och Finland. Något som han är väldigt stolt över är faktan att han är en av dem första utbildade programmeringslärare. Nu har Tobias jobbat på NTI Gymnasiet i snart 8 år och att vara en lärare på NTI Gymnasiet tycker han kan vara jobbigt på grund av alla ämne han har men samtidigt roligt, “NTI Gymnasiet i Södertälje har inte helt fasta traditioner på metoder hur man ska göra saker vilket betyder att det är ganska flexibelt och man kan påverka olika uppgifter.” Säger han. Elever borde välja NTI Gymnasiet om dem letar efter ett gymnasium som ger goda möjligheter och lär ut roliga ämne, skolan är relativt liten vilket innebär att det finns bra relation mellan elever och alla lärare på skolan.</p>
<h3>Per-Anders Karlsson</h3>
<p>Per-Anders, också kallad PA är en lärare på NTI Gymnasiet i Södertälje som undervisar mest för IT elever. Hans uppgift på skolan är att lära ut IT elever allt dem behöver veta om olika nätverksämne och förbereda dem för jobb efter gymnasiet men också högskolestudier för dem som vill det. Det som Per-Anders gillar med just den här skolan är att skolan inte är för stor och att det finns bra relation mellan kollegor men också elever. ‘’Att vara lärare på den här skolan är givande och trevligt, jag trivs jättebra här.” säger PA när han får fråga “Hur det är att vara en lärare på NTI Gymnasiet i Södertälje. Per-Anders Karlsson har jobbat på den här skolan sen år 2016 och han skulle rekommendera den här skolan just på grund av kommunikationsförmåga som finns bland lärare och elever.</p>
